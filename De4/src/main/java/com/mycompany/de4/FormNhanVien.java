    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        BufferedReader br;
        String line;
        try {
            br = new BufferedReader(new FileReader("E:/nhanvien.dat"));
            while((line = br.readLine()) != null){
                String[] part = line.split("\\$");
                if(part.length == 8){
                    NhanVien ds = new NhanVien();
                    ds.setHoTen(part[0]);
                    ds.setNgaySinh(part[1]);
                    ds.setDiaChi(part[2]);
                    ds.setGioiTinh(part[3]);
                    ds.setPb(part[4]);
                    ds.setHesoLuong(Double.parseDouble(part[5]));
                    ds.setThamNien(Integer.parseInt(part[6]));
                    ds.setLuongcb(Double.parseDouble(part[7]));

                    nv.add(ds);
                    ds.hienThi();
                   
                }
            }
            br.close();
            System.out.println("Doc file thanh cong!");
        } catch (IOException ex) {
            System.out.println("Loi khi doc file: " + ex.getMessage());
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

