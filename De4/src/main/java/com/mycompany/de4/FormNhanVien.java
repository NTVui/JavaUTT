    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String hoTen = jTextField3.getText();
        String ngaySinh = jTextField8.getText();
        String diaChi = jTextField2.getText();
        String gioiTinh = "";
        if(jCheckBox1.isSelected()){
            gioiTinh = "Nam";
        }
        if(jCheckBox2.isSelected()){
            gioiTinh = "Nu";
        }
       String pb = jTextField4.getText();
       double hesoLuong = Double.parseDouble(jTextField5.getText());
       int thamNien = Integer.parseInt(jTextField6.getText());
       double luongcb = Double.parseDouble(jTextField7.getText());
       
       //Nếu muốn hiện trên console
//       NhanVien nv1 = new NhanVien(pb, hesoLuong, luongcb, thamNien, hoTen, diaChi, gioiTinh, ngaySinh);
//       nv.add(nv1);
//       nv1.hienThi();
       
        //Đây là lưu file, có thể cắt ra và tạo 1 button lưu, xóa đoạn String line = hoTen +"$"...
        try {
            FileOutputStream fos = new FileOutputStream("E:/nhanvien.dat", true);
            OutputStreamWriter osw = new OutputStreamWriter(fos, "UTF-8");
            BufferedWriter bw = new BufferedWriter(osw);
            //String line = nv.toString();
            String line = hoTen + "$" + ngaySinh + "$" + diaChi + "$" + gioiTinh + "$" + pb + "$" + hesoLuong + "$" + thamNien + "$" + luongcb + "$";
            bw.write(line);
            bw.newLine();
            bw.close();
            fos.close();
            JOptionPane.showMessageDialog(this, "Da luu vao file nhanvien.dat!");
        } catch (FileNotFoundException ex) {
            System.out.println("Loi khong tim thay file!" + ex.getMessage());
        } catch (IOException ex) {
            System.out.println("Loi" + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

